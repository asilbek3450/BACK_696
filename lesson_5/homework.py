

# Uyga vazifa o'tilgan 5-dars uchun:

# 1. Foydalanuvchidan 2 ta son kiritishni so'rang. Bu sonlarning kvadtarlarining ayirmasi kattami
# yoki ayirmasining kvadrati kattami aniqlang. Javobni habar ko`rinishida ekranga chiqaring.











# 2. Foydalanuvchidan 2 ta uchburchaklarning tomonlari a1, b1, c1 va a2, b2, c2 ni
# so'rang. Qaysi bir uchburchakning yuzasi kattaligini aniqlang. Javobni
# habar ko`rinishida ekranga chiqaring. Agar uchburchaklar yuzasi teng bo`lsa,
# "Uchburchaklar yuzasi teng" degan habar chiqsin. Yuzasini qanday topish yuqoridagi darsda ko'rib chiqdik.

# 3. Foydalanuvchidan uchburchakning uch tomoni a, b, c larning qiymatini kiriting. U teng tomonli uchburchak bo`la
# oladimi aniqlang. Agar teng bo'lsa, "Teng tomonli uchburchak" degan habar chiqsin. Agar teng tomonli uchburchak
# bo`lmasa, "Teng tomonli uchburchak emas" degan habar chiqsin. Teng tomonli uchburchak degani 3 ta tomoni ham teng
# bo'lgan uchburchak.

# 4. Foydalanuvchidan uchburchakning uchta a, b, c tomonlarini qiymatini kiriting, uchburchakni to`g`riburchakli
# uchburchak bo`la oladimi yoki yo'qmi aniqlang. (Pifagor teoremasidan foydalaning)
















