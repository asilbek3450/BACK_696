# Git va Github
# Git - bu dasturlashda ishlatiladigan kodlarni versiyalash uchun mo'ljallangan dastur.
# Git orqali nimalar qilsa bo'ladi: Git orqali kodlarni versiyalash, kodlarni boshqa kompyuterlarga ko'chirish, kodlarni boshqa dasturchilarga ulashish.
# Git bilan ishlash uchun Git serveri kerak. Git serveri - bu Git da yozilgan kodlarni saqlash uchun mo'ljallangan server.
# Github - bu Git serveri, ya'ni Git da yozilgan kodlarni saqlash uchun mo'ljallangan server.

# Git bilan ishlash uchun Git dasturini o'rnatish kerak. https://git-scm.com/downloads saytidan o'rnatib olishingiz mumkin.
# Git bilan ishlash uchun terminaldan foydalanish kerak. Terminalni ochish uchun Windows da "Windows + R" tugmalarini bosib "cmd" ni yozing.
# Linux va Mac da terminalni ochish uchun "Ctrl + Alt + T" tugmalarini bosing.
# Terminalda "git --version" ni yozib "Enter" tugmasini bosing. Agar Git o'rnatilgan bo'lsa Git versiyasi chiqadi.
# Git bilan ishlash uchun GitHub saytida ro'yxatdan o'tishingiz kerak.
# GitHub saytida ro'yxatdan o'tgandan so'ng "Create a repository" tugmasini bosing.



